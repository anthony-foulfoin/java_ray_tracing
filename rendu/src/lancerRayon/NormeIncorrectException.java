package lancerRayon;

/** 
 * Exception levee si une norme est incorrecte.
 *
 */
public class NormeIncorrectException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/** 
	* Cree une nouvelle instance de NormeIncorrectException 
	*/  
	public NormeIncorrectException() {}  
	/** 
	* Cree une nouvelle instance de NormeIncorrectException 
	* @param message Le message detaillant exception 
	*/  
	public NormeIncorrectException(String message) {  
		super(message); 
	}  
	/** 
	* Cree une nouvelle instance de NormeIncorrectException 
	* @param cause L'exception a l'origine de cette exception 
	*/  
	public NormeIncorrectException(Throwable cause) {  
		super(cause); 
	}  
	/** 
	* Cree une nouvelle instance de NormeIncorrectException 
	* @param message Le message detaillant exception 
	* @param cause L'exception a l'origine de cette exception 
	*/  
	public NormeIncorrectException(String message, Throwable cause) {  
		super(message, cause); 
	} 
	
}
