package lancerRayon;

public class ValeurNonPositiveException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/** 
	* Cree une nouvelle instance de ValeurNonPositiveException 
	*/  
	public ValeurNonPositiveException() {}  
	/** 
	* Cree une nouvelle instance de ValeurNonPositiveException 
	* @param message Le message detaillant exception 
	*/  
	public ValeurNonPositiveException(String message) {  
		super(message); 
	}  
	/** 
	* Cree une nouvelle instance de ValeurNonPositiveException 
	* @param cause L'exception a l'origine de cette exception 
	*/  
	public ValeurNonPositiveException(Throwable cause) {  
		super(cause); 
	}  
	/** 
	* Cree une nouvelle instance de ValeurNonPositiveException 
	* @param message Le message detaillant exception 
	* @param cause L'exception a l'origine de cette exception 
	*/  
	public ValeurNonPositiveException(String message, Throwable cause) {  
		super(message, cause); 
	} 
}
